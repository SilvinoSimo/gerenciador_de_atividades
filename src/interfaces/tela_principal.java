    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        cadastroBeans cadastroBeans = new cadastroBeans();
        String nome;
      String solicitante;
      int  prioridade = 0 ;
      
      nome = jTextField1.getText();
      solicitante = jTextField2.getText();
      
      if(jComboBox1.getSelectedIndex()== 0){
           prioridade = 1;
          
      }else if(jComboBox1.getSelectedIndex()== 1){
          prioridade = 2 ;
     
      }else if (jComboBox1.getSelectedIndex()== 2){
          prioridade = 3 ;
      }else{
          prioridade = 4 ;
      }
      cadastroBeans.setNome(nome);
      cadastroBeans.setSolicitante(solicitante);
      cadastroBeans.setPrioridade(prioridade);
    }//GEN-LAST:event_jButton1ActionPerformed

    
    
    
    
